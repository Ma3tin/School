    protected static int countTrees_split(String landscape) {
        int x = 0, trees = 0;
        String[] array = landscape.split("\n");
        for (int i = 0; i < array.length; i += 1) {
            if (array[i].charAt(x) == '#') {
                trees++;
            }
            x = (x + 3) % 31;
        }
        return trees;
    }

    protected static int countTrees(String landscape) {
        int x = 0, y = 0, i = 0, trees = 0;
        while (i < landscape.length()) {
            if (landscape.charAt(i) == '#') trees++;
            i = ++x * 32 + (y = (y + 3) % 31);
        }
        return trees;
    }
